public class Task2 {
    public static void main(String[] args) {
        // Примитивный тип (хранит значение)
        int primitiveInt = 42;
        
        // Ссылочный тип (хранит адрес объекта в памяти)
        String referenceString = "Я — ссылочный тип данных (объект)";

        System.out.println("Примитив: " + primitiveInt);
        System.out.println("Ссылка: " + referenceString);
        
        // Комментарий: Примитивы занимают меньше памяти, 
        // а ссылочные типы имеют встроенные методы.
    }
}